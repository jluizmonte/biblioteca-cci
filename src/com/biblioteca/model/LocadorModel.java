package com.biblioteca.model;

/**
 *
 * @author Instrutores
 */
public class LocadorModel {

    private int idLocador;
    private String nomeLocador;
    private String logradouroLocador;
    private String bairroLocador;
    private String cidadeLocador;
    private String estadoLocador;
    private String cepLocador;
    private String telefoneLocador;
    private String emailLocador;

    public int getIdLocador() {
        return idLocador;
    }

    public void setIdLocador(int idLocador) {
        this.idLocador = idLocador;
    }

    public String getNomeLocador() {
        return nomeLocador;
    }

    public void setNomeLocador(String nomeLocador) {
        this.nomeLocador = nomeLocador;
    }

    public String getLogradouroLocador() {
        return logradouroLocador;
    }

    public void setLogradouroLocador(String logradouroLocador) {
        this.logradouroLocador = logradouroLocador;
    }

    public String getBairroLocador() {
        return bairroLocador;
    }

    public void setBairroLocador(String bairroLocador) {
        this.bairroLocador = bairroLocador;
    }

    public String getCidadeLocador() {
        return cidadeLocador;
    }

    public void setCidadeLocador(String cidadeLocador) {
        this.cidadeLocador = cidadeLocador;
    }

    public String getEstadoLocador() {
        return estadoLocador;
    }

    public void setEstadoLocador(String estadoLocador) {
        this.estadoLocador = estadoLocador;
    }

    public String getCepLocador() {
        return cepLocador;
    }

    public void setCepLocador(String cepLocador) {
        this.cepLocador = cepLocador;
    }

    public String getTelefoneLocador() {
        return telefoneLocador;
    }

    public void setTelefoneLocador(String telefoneLocador) {
        this.telefoneLocador = telefoneLocador;
    }

    public String getEmailLocador() {
        return emailLocador;
    }

    public void setEmailLocador(String emailLocador) {
        this.emailLocador = emailLocador;
    }

}
